<h1>Что я делаю в школе на переменах</h1>
<p>
	На переменах мы с друзьями гуляем по школе всей компанией. Иногда на переменах
	нам приходится доделывать уроки,которые не сделал дома. А так же,на переменах
	мы любим заходить в столовую. После уроков мы сразу бежим в столовую кушать.   
</p>   
<p>
	В школе у нас много знакомых, и с каждым приходится здороваться.
</p>
